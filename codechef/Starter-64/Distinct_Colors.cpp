void solve()
{
    int n;
    cin >> n;
    int a[n];
    input(a, 0, n-1);
    sort(a, a+n);
    int ans = a[0];
    for(int i = 1; i < n; i++){
        ans += a[i] - a[i-1];
    }
    cout << ans << endl;
}
